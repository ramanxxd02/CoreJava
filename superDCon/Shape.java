package in.co.superDCon;

public class Shape {
	private String colour;
	private int borderwidth;
	public Shape(String colour,int borderwidth) {
		this.colour=colour;
		this.borderwidth=borderwidth;
	}
	public String getcolour() {
		return colour;
	}
	public int getborderwidth() {
		return borderwidth;
	}
}
