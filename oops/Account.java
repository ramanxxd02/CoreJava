package in.co.oops;
//Encapsulation  get set method
public class Account {
	private String name;
	private int salary;
	private int age;

	public String getname() {
		return name;
	}
	public int getsalary() {
		return salary;
	}
	public int getage() {
		return age;
	}
	public void setName(String name) {
		this.name=name;
	}
	public void setSalary(int salary) {
		this.salary=salary;
	}
	public void setAge(int age) {
		this.age=age;
	}
 
}
